var reviewsIndex = 0
$(".next-btn").on("click",function(){
  reviewsIndex++;
  call(reviewsIndex);
  if (reviewsIndex>=reviews.length-1){
    reviewsIndex=-1
  }
})

// reviewsIndex2 = (reviews.length)-1;
$(".prev-btn").on("click",function(){
  if (reviewsIndex<=0){
    reviewsIndex=reviews.length;
  }
  reviewsIndex--;
  call(reviewsIndex);
})


$(".random-btn").on("click",function(){
  var randomNumber = Math.floor(Math.random()*(reviews.length));
  call(randomNumber)
})

function call(person){
  $("#author").text(reviews[person].name);
  $("#job").text(reviews[person].job);
  $("#person-img").attr("src",reviews[person].img);
  $("#info").text(reviews[person].text);
}